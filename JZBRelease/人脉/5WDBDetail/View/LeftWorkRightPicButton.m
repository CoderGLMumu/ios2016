#import "LeftWorkRightPicButton.h"

@implementation LeftWorkRightPicButton

- (instancetype)initWithFrame:(CGRect)frame
{
    if (self = [super initWithFrame:frame]) {
        self.titleLabel.textAlignment = NSTextAlignmentCenter;
        self.titleLabel.font = [UIFont systemFontOfSize:12];
        [self setTitleColor:[UIColor hx_colorWithHexRGBAString:@"BDBDBD"] forState:UIControlStateNormal];
    }
    return self;
}

- (void)layoutSubviews
{
    [super layoutSubviews];
    
    self.imageView.glcx_centerX = self.glw_width * 0.5 + 25;
    
    self.titleLabel.glcx_centerX = self.glw_width * 0.5 - 10;

}

@end
